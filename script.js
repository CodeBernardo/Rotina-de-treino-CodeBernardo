for (let i = 0; i < workOuts.length; i++) {
  const main = document.querySelector("main");

  const section = document.createElement("section");
  section.classList.add("container-dia");
  section.id = "dia_" + [i];

  const h2 = document.createElement("h2");
  h2.innerText = `${workOuts[i].diaSemana}`;

  section.append(h2);
  main.appendChild(section);

  const sectionId = document.getElementById("dia_" + [i]);
  const sectiontittle = sectionId.querySelector("h2");

  sectiontittle.addEventListener("click", function (event) {
    const allElements = sectionId.querySelectorAll("div");

    for (let i = 0; i < allElements.length; i++) {
      allElements[i].classList.toggle("hidden");
    }
  });

  for (let j = 0; j < workOuts[i].treinoDia.length; j++) {

    const div = document.createElement("div");
    div.classList.add("row", "hidden");
    
    const imgContainer = document.createElement('figure')
    imgContainer.classList.add('img-container', 'hidden')
    
    const img = document.createElement('img')
    img.src = workOuts[i].treinoDia[j].imagemTreino
    
    imgContainer.appendChild(img)
    section.appendChild(div);
    
    const p = document.createElement("p");
    p.innerText = `${workOuts[i].treinoDia[j].exercicio}: ${workOuts[i].treinoDia[j].series}`;
    

    p.addEventListener('click', function(event) {
      imgContainer.classList.toggle('hidden')
    })
    
    const label = document.createElement("label");
    const input = document.createElement("input");
    input.type = "checkbox";
    
    label.appendChild(input);
    div.append(p, label,imgContainer);
  }
}


